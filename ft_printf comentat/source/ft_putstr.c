#include "../ft_printf.h"

int	ft_putstr(char *s)
{
	int	i;

	i = 0;
	if (s)
	{
		while (s[i] != '\0')
		{
			if (write(1, &s[i], 1) != 1)
				return (-1);
			i++;
		}
	}
	if (!s)
		return (write(1, "(null)", 6));
	return (i);
}
